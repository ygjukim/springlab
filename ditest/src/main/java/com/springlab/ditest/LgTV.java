package com.springlab.ditest;

public class LgTV {

	public void turnOn() {
		System.out.println("LG TV : 전원 켠다.");
	}
	
	public void turnOff() {
		System.out.println("LG TV : 전원 끈다.");
	}
	
	
	public void soundUp() {
		System.out.println("LG TV : 소리를 높인다.");
	}
	
	public void soundDown() {
		System.out.println("LG TV : 소리를 낮춘다.");
	}
	
}
